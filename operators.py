# Arithmetic Operators - Perform simple calculations
x = 45
y = 21

print(x + y)
print(x - y)
print(x * y)
print(x / y)
print(x % y) # modulus-returns the remainder

# Comparison Operators
a = 56
b = 2
print(a < b)
print(a > b)
print(a <= b)
print(a >= b)
print(a == b) # Equal to
print(a != b) # Not equal to

# Assignment opertors
z = 6
print(z)

num = 2
num += 10
print(num)

# logical Operators
num1 = 5
num2 = 10
print(num1 < num2 and num1 == num2)
print(num1 < num2 or num1 == num2)
print(not(num1 < num2 and num1 == num2))

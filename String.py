text = "hello"
text1 = "THERE"

print(text)
# Accessing an element in a string
print(text[1])
print(text[2])

# Modifying a string
print(text.upper())
print(text1.lower())

# Size/length of string
print(len(text))

# String Concatenation - Combining strings
print(text + text1)
print(text + " " + text1)

# Assignment
# 1. Reassign a string
thislist = ["apple", "banana", "cherry"]
thislist[1] = "blackcurrant"
print(thislist)
# 2. Deleting a string
list1 = ["apple", "banana", "cherry"]
thislist.remove("banana")
print(list1)

